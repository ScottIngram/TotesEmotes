local ADDON_NAME, TOTES = ...
local L10N = TOTES.L10N

local memoized = {}

function TOTES.localizeEmote(key)
    assert(key, "key must not be nil")
    if memoized[key] then
        return memoized[key]
    end

    local slashCmd = TOTES.EMOTES_LOCALIZED[key] or L10N.MISSING_EMOTES[key]
    assert(slashCmd, "unknown key: "..key)
    slashCmd = string.lower(slashCmd)
    local noSlash = string.sub(slashCmd, 2)
    if string.sub(slashCmd,1,1) == "/" then
        noSlash = string.sub(slashCmd, 2)
    else
        noSlash = slashCmd
    end

    memoized[key] = noSlash
    return noSlash
end

